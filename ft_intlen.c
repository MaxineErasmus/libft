#include "libft.h"

int	ft_intlen(const int n)
{
	int	i;
	int	num;

	i = 0;
	num = n;
	if (num == 0)
		return (1);
	if (num < 0)
		num *= -1;
	while (num > 0)
	{
		num /= 10;
		i++;
	}
	return (i);
}
